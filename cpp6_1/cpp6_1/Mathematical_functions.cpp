#include "Mathematical_functions.h"

int sum(const int& x, const int& y) {
	return x + y;
}

int sub(const int& x, const int& y){
	return x - y;
}

int multi(const int& x, const int& y){
	return x*y;
}

int divis(const int& x, const int& y){
	return x/y;
}

int exponent(const int& x){
	return x*x;
}
